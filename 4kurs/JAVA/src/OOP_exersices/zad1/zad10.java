package OOP_exersices.src.zad1;

import java.util.Scanner;

public class zad10 {
    public static void main(String[] args) {
        Scanner myScanner = new Scanner(System.in);
        int n = myScanner.nextInt();
        if(n > 0){
            for (int i = 1; i <= n; i++) {
                System.out.println(i);
            }
        }

    }
}